int		ft_sqrt(int nb)
{
	int		sqr;

	sqr = 1;
	if (nb < 0)
		return 0;
	while (sqr * sqr != nb)
	{
		sqr++;
		if(sqr >= nb)
			return(0);
	}
	return sqr;
}
